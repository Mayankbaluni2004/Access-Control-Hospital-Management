    public void ChannelTable()
    {
        try {
            pst=con.prepareStatement("select channel.channelno,doctor.dname,patient.Name,channel.roomno,channel.date from doctor INNER JOIN channel on channel.dID=doctor.doctorno INNER JOIN patient on channel.pID=patient.PatientNo where doctor.log_id=?");   
            pst.setInt(1,newid);
           
            rs=pst.executeQuery();        
            java.sql.ResultSetMetaData rsm=rs.getMetaData();
            int c;
            
            c=rsm.getColumnCount();
            
            DefaultTableModel df=(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 =new Vector();
                
                for(int i=1;i<=c;i++){
                    v2.add(rs.getString(1));
                    v2.add(rs.getString(2));
                    v2.add(rs.getString(3));
                    v2.add(rs.getString(4));
                    v2.add(rs.getString(5));
                }
                
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Channel.Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
