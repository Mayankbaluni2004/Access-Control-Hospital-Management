    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txtName.getText();
        String username = txtUserName.getText();
        String password = txtPassword.getText();
        String usertype = txtUserType.getSelectedItem().toString();
       
        if (name.isEmpty() || username.isEmpty() || password.isEmpty() || usertype.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all details.");
            return;
        }
        try {
            pst=con.prepareStatement("INSERT INTO hospital.users(name,username,password,usertype)values(?,?,?,?)");
            pst.setString(1,name);
            pst.setString(2,username);
            pst.setString(3,password);
            pst.setString(4,usertype);
            
            pst.executeUpdate();
        
            JOptionPane.showMessageDialog(this,"User Inserted !!");
            
            txtName.setText("");
            txtUserName.setText("");
            txtPassword.setText("");
            txtUserType.setSelectedIndex(-1);
            txtName.requestFocus();
            
        } catch (SQLException ex) {
            Logger.getLogger(user.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

