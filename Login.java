    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txtUserName.getText();
        String password = txtPassword.getText();
        String usertype = txtUserType.getSelectedItem().toString();
        Connect();

        try {
            pst = con.prepareStatement("SELECT * FROM users WHERE username = ? AND password = ? AND userType = ?");
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, usertype);

            rs = pst.executeQuery();
            if (rs.next()) {
                int userid = rs.getInt("id");
                this.setVisible(false);

                new Main(userid, username, usertype).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid UserName or Password");
                txtUserName.setText("");
                txtPassword.setText("");
                txtUserType.setSelectedIndex(-1);
                txtUserName.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

